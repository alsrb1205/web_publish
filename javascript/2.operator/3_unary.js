// 단항 연산자 : ++, --, !, !!

let a =10;
console.log(++a); //11
console.log(a++); //11을 출력후 1 증가
console.log(a); //12

let b =10;
console.log(--b); //9
console.log(b--); //9을 출력후 1 감소
console.log(b); //8

let flag =true;
console.log(flag);
console.log(!flag); // ! : not
console.log(!!flag); // !(!flag)




