/**
 * 공통 모듈 저장
 */
export default function sum(number1, number2) { return parseInt(number1) + parseInt(number2) }; // 자주 사용하는 함수는 dafault로
export function sub(number1, number2) { return parseInt(number1) - parseInt(number2) };
export function mul(number1, number2) { return parseInt(number1) * parseInt(number2) };
export function div(number1, number2) { return parseInt(number1) / parseInt(number2) };
